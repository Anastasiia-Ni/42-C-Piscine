int		ft_iterative_factorial(int nb)
{
	int i;
	int fact;

	fact = 1;
	if (nb < 0)
	{
		return (0);
	}
	else if (nb == 0)
	{
		return (1);
	}
	else
	{
		i = 1;
		while (i <= nb)
		{
			fact *= i;
			i++;
		}
	}
	return (fact);
}
